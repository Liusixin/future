package cn.indispensable.future.async;

import java.util.HashMap;
import java.util.Map;

/**
 * 存储事件的属性
 * @author cicicc
 * @since 0.0.1
 */
public class EventModel {
    private EventType type; //指定事件的类型
    private int actorId; //事件发起者id
    private int entityType;//事件被操作对象的类型
    private int entityId; //被操作对象的id
    private int entityOwnerId;//被操作对象所属的用户,比如问题的提问者是谁

    private Map<String, String> exts = new HashMap<String, String>();

    public EventModel addExt(String key, String value) {
        exts.put(key, value);
        return this;
    }

    public String getExt(String key) {
        return exts.get(key);
    }

    public EventModel() {
    }

    public EventModel(EventType type) {
        this.type = type;
    }

    public EventType getType() {
        return type;
    }

    public EventModel setType(EventType type) {
        this.type = type;
        return this;
    }

    public int getActorId() {
        return actorId;
    }

    public EventModel setActorId(int actorId) {
        this.actorId = actorId;
        return this;
    }

    public int getEntityType() {
        return entityType;
    }

    public EventModel setEntityType(int entityType) {
        this.entityType = entityType;
        return this;
    }

    public int getEntityId() {
        return entityId;
    }

    public EventModel setEntityId(int entityId) {
        this.entityId = entityId;
        return this;
    }

    public int getEntityOwnerId() {
        return entityOwnerId;
    }

    public EventModel setEntityOwnerId(int entityOwnerId) {
        this.entityOwnerId = entityOwnerId;
        return this;
    }

    public Map<String, String> getExts() {
        return exts;
    }

    public EventModel setExts(Map<String, String> exts) {
        this.exts = exts;
        return this;
    }
}
